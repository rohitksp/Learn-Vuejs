<template>
  <div>
    <h1>{{ animal.name }}</h1>
    <h1>{{ animal.age }}</h1>
    <h1>{{ animal.breed }}</h1>
  </div>
</template>

<script>
import { mapState } from 'vuex'

export default {
  data () {
    return {
      animal: {}
    }
  },
  computed: {
    ...mapState([
      'cats',
      'dogs'
    ])
  },
  mounted () {
    const animal = this[this.$route.params.species][this.$route.params.id]
    this.animal = animal
  }
}
</script>
